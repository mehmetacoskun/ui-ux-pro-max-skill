-- --------------------------------------------
-- 4.1) transfer_credits — Kredi Transferi
-- --------------------------------------------
-- 📌 HATIRLATMA: Bu fonksiyon yalnızca status='completed' olan talepler için çalışır.
--    Flutter'da kullanım sırası:
--      1. service_request'i 'completed' yap
--      2. transfer_credits RPC'sini çağır:
--         await supabase.rpc('transfer_credits', params: {'p_service_request_id': requestId});
--
-- ⚠️  UYARI: Bu fonksiyonu doğrudan tarayıcıdan veya Postman'den çağırmayın.
--    SECURITY DEFINER ile çalışır, yani DB sahibi yetkisiyle execute eder.
--    RLS poliçesinde bu fonksiyona kimin erişebileceği kısıtlanacak.

CREATE OR REPLACE FUNCTION public.transfer_credits(p_service_request_id BIGINT)
RETURNS VOID AS $$
DECLARE
  v_requester_id UUID;
  v_provider_id  UUID;
  v_credits      INTEGER;
  v_requester_balance INTEGER;
  v_provider_balance  INTEGER;
BEGIN
  SELECT requester_id, provider_id, credits_amount
  INTO v_requester_id, v_provider_id, v_credits
  FROM public.service_requests
  WHERE id = p_service_request_id AND status = 'completed';

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Service request not found or not completed (id: %)', p_service_request_id;
  END IF;

  -- Bakiye kontrolü (kredi 0'ın altına düşemez)
  SELECT credits INTO v_requester_balance FROM public.profiles WHERE id = v_requester_id FOR UPDATE;
  IF v_requester_balance < v_credits THEN
    RAISE EXCEPTION 'Insufficient credits. Required: %, Available: %', v_credits, v_requester_balance;
  END IF;

  -- Alıcıdan düş
  UPDATE public.profiles SET credits = credits - v_credits WHERE id = v_requester_id;
  SELECT credits INTO v_requester_balance FROM public.profiles WHERE id = v_requester_id;
  INSERT INTO public.credit_transactions (user_id, amount, type, service_request_id, balance_after, description)
  VALUES (v_requester_id, -v_credits, 'service_received', p_service_request_id, v_requester_balance, 'Service payment');

  -- Vericiye ekle
  UPDATE public.profiles SET credits = credits + v_credits WHERE id = v_provider_id;
  SELECT credits INTO v_provider_balance FROM public.profiles WHERE id = v_provider_id;
  INSERT INTO public.credit_transactions (user_id, amount, type, service_request_id, balance_after, description)
  VALUES (v_provider_id, v_credits, 'service_given', p_service_request_id, v_provider_balance, 'Service earning');

END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- --------------------------------------------
-- 4.2) check_credits — Kredi Yeterlilik Kontrolü
-- --------------------------------------------
-- 📌 HATIRLATMA: Flutter, service_request oluşturmadan ÖNCE bu fonksiyonu çağırmalı.
--    Yetersiz bakiyede kullanıcıya hata gösterilmeli, INSERT yapılmamalıdır.
--    Kullanım:
--      final result = await supabase.rpc('check_credits',
--        params: {'p_user_id': userId, 'p_required': creditCost});
--      if (result == false) { // Hata göster }

CREATE OR REPLACE FUNCTION public.check_credits(p_user_id UUID, p_required INTEGER)
RETURNS BOOLEAN AS $$
DECLARE
  v_balance INTEGER;
BEGIN
  SELECT credits INTO v_balance FROM public.profiles WHERE id = p_user_id;
  RETURN v_balance >= p_required;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


