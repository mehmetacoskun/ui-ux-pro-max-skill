-- AŞAMA 5 — pg_cron: 48 SAAT OTOMATİK EXPIRE
-- ============================================
-- 📌 ÖN KOŞULLAR (önce bunları yap!):
--    1. Supabase Dashboard > Database > Extensions > pg_cron'u aktif et
--    2. Free tier kullanıyorsan bu adımı atlayabilirsin;
--       bunun yerine Supabase Edge Function + Scheduled Trigger kullan.
--
-- 📌 Flutter'da SERVICE REQUEST INSERT yaparken:
--    'expires_at': DateTime.now().toUtc().add(Duration(hours: 48)).toIso8601String()
--    Bunu set etmezsen aşağıdaki job çalışmaz!
--
-- ⚠️  UYARI: Bu SELECT'i yalnızca pg_cron aktifken çalıştır.
--    Aksi takdirde 'function cron.schedule does not exist' hatası alırsın.

SELECT cron.schedule(
  'expire-pending-service-requests',   -- job adı (benzersiz olmalı)
  '*/10 * * * *',                      -- her 10 dakikada bir çalışır
  $$
    UPDATE public.service_requests
    SET status = 'expired', updated_at = NOW()
    WHERE status = 'pending'
      AND expires_at IS NOT NULL
      AND expires_at < NOW();
  $$
);


-- ============================================
-- AŞAMA 6 — RLS (HENÜZ YAZILMADI)
-- ============================================
-- 📌 RLS poliçeleri ayrı bir dosyada (RLS_Policies.sql) yazılacak.
--    Şema kurulumu tamamlanınca Faz 2'de uygulanacak.
--
-- Planlanan kısıtlamalar (4 rol: user / moderator / admin / superadmin):
--   profiles        → Herkes SELECT | Sadece kendisi UPDATE | admin+superadmin tümünü görür
--   categories      → Herkes SELECT | Sadece admin & superadmin INSERT/UPDATE/DELETE
--   services        → Herkes SELECT (active) | Sadece sahibi yazar | mod/admin/superadmin status günceller
--   service_requests→ Sadece requester/provider okur & yazar | mod/admin/superadmin listeler (salt okunur)
--   reviews         → Onaylananlar herkese SELECT | Sadece reviewer yazar | mod/admin/superadmin status günceller
--   credit_trans.   → Sadece kendisi SELECT | Kimse doğrudan yazamaz | admin+superadmin listeler
--   rol atama       → moderator rolunu admin & superadmin verir; admin rolünü yalnızca superadmin verir
